package com.flamematch.app.data.model

import com.google.firebase.Timestamp
import com.google.firebase.firestore.GeoPoint

data class User(
    val id: String = "",
    val email: String = "",
    val name: String = "",
    val age: Int = 18,
    val gender: String = "",
    val bio: String = "",
    val photos: List<String> = emptyList(),
    val profilePhoto: String = "",
    val location: GeoPoint? = null,
    val city: String = "",
    val country: String = "",
    val lookingFor: String = "everyone",
    val minAge: Int = 18,
    val maxAge: Int = 50,
    val maxDistance: Int = 50,
    val isPremium: Boolean = false,
    val premiumType: String? = null,
    val premiumExpiry: Timestamp? = null,
    val isVerified: Boolean = false,
    val verificationPhoto: String? = null,
    val voiceVibe: String? = null,
    val interests: List<String> = emptyList(),
    val likesCount: Int = 0,
    val superLikesCount: Int = 0,
    val boostsCount: Int = 0,
    val dailyLikesRemaining: Int = 10,
    val dailySuperLikesRemaining: Int = 1,
    val createdAt: Timestamp? = null,
    val lastActive: Timestamp? = null,
    val lastBoost: Timestamp? = null,
    val showOnlineStatus: Boolean = true,
    val showDistance: Boolean = true,
    val pushNotifications: Boolean = true,
    val fcmToken: String? = null
) {
    constructor() : this(id = "")
    
    fun toMap(): Map<String, Any?> = mapOf(
        "id" to id,
        "email" to email,
        "name" to name,
        "age" to age,
        "gender" to gender,
        "bio" to bio,
        "photos" to photos,
        "profilePhoto" to profilePhoto,
        "location" to location,
        "city" to city,
        "country" to country,
        "lookingFor" to lookingFor,
        "minAge" to minAge,
        "maxAge" to maxAge,
        "maxDistance" to maxDistance,
        "isPremium" to isPremium,
        "premiumType" to premiumType,
        "premiumExpiry" to premiumExpiry,
        "isVerified" to isVerified,
        "verificationPhoto" to verificationPhoto,
        "voiceVibe" to voiceVibe,
        "interests" to interests,
        "likesCount" to likesCount,
        "superLikesCount" to superLikesCount,
        "boostsCount" to boostsCount,
        "dailyLikesRemaining" to dailyLikesRemaining,
        "dailySuperLikesRemaining" to dailySuperLikesRemaining,
        "createdAt" to createdAt,
        "lastActive" to lastActive,
        "lastBoost" to lastBoost,
        "showOnlineStatus" to showOnlineStatus,
        "showDistance" to showDistance,
        "pushNotifications" to pushNotifications,
        "fcmToken" to fcmToken
    )
}
